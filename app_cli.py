import pandas as pd
import matplotlib.pyplot as plt
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error, r2_score

data = {
    "sqft": [1000, 1500, 2000, 2500, 3000, 3500, 4000, 4500, 5000, 5500, 6000, 6500, 7000, 7500, 8000],
    "price": [1500000, 2250000, 3000000, 3750000, 4500000, 5250000, 6000000, 6750000, 7500000, 8250000, 9000000, 9750000, 10500000, 11250000, 12000000]
}
df=pd.DataFrame(data)
print(df)

a=df[['sqft']]#a=area
p=df[['price']]#p=price

a_train,a_test,p_train,p_test=train_test_split(a,p,test_size=0.2,random_state=42)

model=LinearRegression()
model.fit(a_train,p_train)

price_pred=model.predict(a_test)
print(price_pred)

print("Mean Squared Error:",mean_squared_error(p_test,price_pred))#less accurate then r2 score
print("R2 Score:",r2_score(p_test,price_pred))#to check accuracy

area=int(input("enter the area of land(in sqft):"))
price=model.predict([[area]])
print("predicted price:",price[0])

plt.scatter(a,p,color='blue',label='Actual Price',marker='*')
plt.plot(a,model.predict(a),color='red',label='Predicted Price')
plt.legend()
plt.show()
